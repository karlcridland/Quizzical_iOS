    func scene(_ scene: UIScene, openURLContexts URLContexts: Set<UIOpenURLContext>) {
        if Settings.shared.defaults.value(forKey: "id") == nil{
            Firebase.shared.newUser()
        }
        for context in URLContexts{
            if context.url.query != nil{
                let join = JoinQuiz()
                window?.rootViewController?.view.addSubview(join)
                join.input(context.url.query!)
                join.submit()
            }
